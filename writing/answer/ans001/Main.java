package javaExercises.writing.answer.ans001;

public class Main {
    public static void main(String[] args) {
        int n = 3;
        int m = 5;
        java.lang.System.out.println("　和: " + (n + m));
        java.lang.System.out.println("　差: " + (n - m));
        java.lang.System.out.println("　積: " + (n * m));
        java.lang.System.out.println("　商: " + (n / m));
        java.lang.System.out.println("余り: " + (n % m));
    }
}
